public class Psto42 {
}

class Solution {
    public int maxSubArray(int[] nums) {

        int temp = 0;
        int maxSum = nums[0];

        for (int num : nums) {
            temp = Math.max(temp + num, num);
            maxSum = Math.max(maxSum, temp);
        }

        return maxSum;

    }
}